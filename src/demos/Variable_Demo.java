package demos;

public class Variable_Demo {

	public static void main(String[] args) {
		// integer data types
		byte age = 25;
		short marks = 500;
		int salary = 10000;
		long id = 11111;
		double height = 5.5;
		System.out.println(age);
		System.out.println(marks);
		System.out.println(salary);
		System.out.println(id);
		System.out.println(height);
		

	}

}
